# inputs
N1 = int(input('Digite um número (1):'))
N2 = int(input('Digite um número (2):'))

# Operações pré-definidas
S = N1 + N2
D = N1 / N2
M = N1 * N2

print('A soma entre {} e {} equivale à: {}'.format( N1, N2, S))
print('A divisão entre {} e {} equivale à: {}'.format( N1, N2, D))
print('A multiplicação entre {} e {} equivale à: {}'.format( N1, N2, M))